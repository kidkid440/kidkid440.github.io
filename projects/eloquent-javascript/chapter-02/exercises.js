function drawChessboard(changeSize) {
  var game = '';

for(var e = 0; e < changeSize; e++){
for(var s = 0; s < changeSize; s++){
    if((s + e) % 2 === 0){
        game = game + ' ';
    }else {
        game = game + '#';
    }
}
game += '\n';
}
console.log(game);


}

