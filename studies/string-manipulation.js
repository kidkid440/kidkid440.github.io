/*
* 1. Manipulating strings using operators such as += and +, will take strings and put them together.
*   Ex.1   var str = 'Go,';
*         var str2 = " " + 'Saints';   str2 = 'Go, Saints'
*  
*   Ex.2   var str = 'Go,';
*           str += " " + 'Saints';    str = 'Go, Saints'
*/

/*
* String Methods use built in methods to modify strings. We use Methods as a way not to invent the wheel.
*
* 1. .length- Gives the length of the string. Ex. 'Hello!'.length   = 6
*
* 2. .split()- Takes a string and turns it to a ARRAY. Ex. 'Hello!'.split() =   ['Hello!']
*                                                           'Hello!'.split('') =  ["H", "e", "l", "l", "o", "!"]
*
* 3. .concat()- Takes two or more strings and joins them together. Ex.   var str = 'Go,'
*                                                                        str.concat(' ', "Saints!")
*
* 4. .toUpperCase()/.toLowerCase()- Changes the Case of Strings  Ex.   var str = "stafford"
*                                                                str.toUpperCase() =   "STAFFORD"     
*
* 5. .slice(start, end)- returns from a string whatever inbetween to set index values. Ex. var str = 'hello my name is stafford'
*                                                                                          str.slice(17, 25);
*
* 6. .charAt()- returns the character at the given Index.  Ex.   var str = "hello my name is stafford"
*                                                                str.charAt([9])
*/ 









